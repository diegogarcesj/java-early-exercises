/**
 * Muestra los números de 320 a 160, contando de 20 en 20 hacia atrás, usando el bucle for.
 * 
 * @author Diego Garcés Jiménez
 */

public class De320A160De20En20For {
	public static void main(String[] args) {
		
		for (int i = 320; i >= 160; i -= 20) { // i -= 20 equivale a i = i - 20
			System.out.println(i);
		}
	}
}
