public class EjerVarEnteras {
	public static void main(String[] args) {
		
		int x = 144;
		int y = 999;
		
		int sum = x + y;
		int res = x - y;
		int mul = x * y;
		int div = x / y;
		
		System.out.println("x vale " + x + " e y vale " + y);
		System.out.println("La suma es " + sum + " y la resta es " + res);
		System.out.println("La multiplicación es " + mul + " y la división entera es " + div);
	}
}
